public class DoCategory implements IBaseEntity{

    private int id;

    private String name;

    public DoCategory() {
    }

    public DoCategory(int id, String name) {
        this.id = id;
        this.name = name;
    }

    @Override
    public int getId() {
        return id;
    }

    @Override
    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
